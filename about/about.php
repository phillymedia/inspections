							<p><i>Clean Plates</i> allows you to look up food safety inspection reports for the past several years in Philadelphia and Bucks County. Data for the project was obtained from the food safety offices in the <a href="http://www.phila.gov/health/foodprotection/index.html" target="_blank" style="text-decoration: underline">city</a> (with a mandatory 30-day delay) and <a href="http://www.buckscounty.org/government/healthservices/HealthDepartment/Environmental/FoodEstablishment" target="_blank" style="text-decoration: underline">county</a> (posted within one day).</p>
							<p>Routine inspections are conducted at <a href="http://www.philly.com/philly/health/special_reports/clean-plates/How_Often_Food_Establishments_Are_Inspected.html" target="_blank" style="text-decoration: underline">regular intervals</a>; repeat visits are common, depending on violations found. Any inspection represents only a snapshot of conditions that are constantly changing; a pattern over time may suggest more than one good or bad report. The city explains the basics of inspection reports <a href="http://www.phila.gov/health/pdfs/UnderstandingtheFoodFacilityInspectionReport_12-1-14%20final.pdf" target="_blank style="text-decoration: underline"">here</a>. </p>
							<p>Inspection reports are dense documents, so this graphic gives tips on how to read them And the city health department offers its own suggestions <a href="http://www.phila.gov/health/pdfs/UnderstandingtheFoodFacilityInspectionReport_12-1-14%20final.pdf" target="_blank" style="text-decoration: underline">here</a>.</p>
							<p>While the inspections have long been available online, our goal is to make them more accessible and easier to understand.</p>
							<p>We add an estimated 60 new inspections a day in Philadelphia, covering restaurants, food trucks, schools, nursing homes, and even jails.</p>
							<p>We also detail the state of food at key Philly locations, such as the Reading Terminal, The Airport, the stadiums and the restaurants of star chefs from Marc Vetri to Michael Solomonov to Jose Garces. In later iterations, we will add inspections in additional suburban counties.</p>
