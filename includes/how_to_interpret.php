							<i>Items on inspection reports are divided into two categories:</i><br/>
							<ul>
								<li>"Foodborne Illness Risk Factors" (top half of the form) are food establishments' practices or procedures that scientists say play a direct role in transmitting germs. These are given higher priority during inspections. Repeat problems are taken more seriously; those that are fixed through public health interventions during the visit, less so. Specific  violations or combinations of violations may trigger reinspections, which are common.</li>
								<li>"Good Retail Practices" (bottom half) are given a lower priority. Research suggests that they can prevent the conditions that lead to contamination but do not cause illness directly. But a high number of violations may indicate a problem.</li>
							</ul>
							<p>In addition, asterisks (*) on Bucks County reports indicate violations that are considered critical.</p>
